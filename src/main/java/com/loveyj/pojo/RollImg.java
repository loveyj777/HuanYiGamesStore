package com.loveyj.pojo;


public class RollImg {

  private int id;
  private int gid;
  private String imgFile;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getGid() {
    return gid;
  }

  public void setGid(int gid) {
    this.gid = gid;
  }

  public String getImgFile() {
    return imgFile;
  }

  public void setImgFile(String imgFile) {
    this.imgFile = imgFile;
  }
}
